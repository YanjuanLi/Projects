    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = caseTable.getSelectedRow();
        if(selectedRow>=0){
            int selectionResult = JOptionPane.showConfirmDialog(null,"Are you sure to delete this elder?");
            if(selectionResult == JOptionPane.YES_OPTION){
               Case c = (Case)caseTable.getValueAt(selectedRow, 0);
                for(Network network:system.getNetworkList()){
                  for(NursingHouse nh:network.getNursingHouseDirectory().getNursingHouseList()){
                      if(nh.getName().equals(institution)){
                           
                            
                                
                                    nh.getCaseDirectory().deleteCase(c);
                                
                            
                        }
                    }
                }
                
                
            }
                
              populate();
            
        }else{
             JOptionPane.showMessageDialog(null, "Please select a case!");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed


