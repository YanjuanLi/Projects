    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:
         int selectedRow = caseTable.getSelectedRow();
        if(selectedRow>=0){
            
            
                Case c = (Case)caseTable.getValueAt(selectedRow, 0);
                
               ViewCaseJPanel panel = new ViewCaseJPanel(container,system,c,institution);
               container.add("ViewCaseJPanel",panel);
               CardLayout layout =(CardLayout)container.getLayout();
               layout.next(container);
            
        }else{
             JOptionPane.showMessageDialog(null, "Please select a case!");
        }
        
    }//GEN-LAST:event_viewBtnActionPerformed

