    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
        String applicantName = applicantNameText.getText();
        int applicantAge = Integer.parseInt(applicantAgeText.getText());
        String applicantGender = applicantGenderText.getText();
        String homeAddress = homeAddressText.getText();
        String phoneNumber = homeAddressText.getText();
        String email = emailText.getText();
        
        String nursingHouseName = nursingHouseText.getText();
        String location = Location.getText();
        String scale = scaleText.getText();
        String level = (String)levelCombox.getSelectedItem();
        
        NursingHouse nh = null;
        boolean b=false;
        
        for(Network network:system.getNetworkList()){
            b=network.getNursingHouseDirectory().checkIfNursingHouseIsUnique(nursingHouseName);
        }
        if(b==true){
        if(level.equals("Low Level")){
           nh=system.getNursingHouseDirectory().createAndAddNursingHouse(nursingHouseName, "Low Level");
        }else if(level.equals("Middle Level")){
           nh=system.getNursingHouseDirectory().createAndAddNursingHouse(nursingHouseName, "Middle Level");
        }else{
           nh=system.getNursingHouseDirectory().createAndAddNursingHouse(nursingHouseName, "High Level");
        }
        
        
        nh.setLocation(location);
        nh.setScale(scale);
        nh.setStatus(0);
        
        AdministratorAccount aa = nh.getAdministratorAccountDirectory().addAdministratorAccount();
        
        Administrator a = new Administrator();
        aa.setAdministrator(a);
        
        aa.setUsername(applicantName);
       
        a.setAge(applicantAge);
        a.setEmail(email);
        a.setGender(applicantGender);
        a.setHomeAddress(homeAddress);
        a.setPhoneNumber(phoneNumber);
        a.setName(applicantName);
        
        JOptionPane.showMessageDialog(null, "Apply Successfully!");
        applicantNameText.setText("");
        applicantAgeText.setText("");
        applicantGenderText.setText("");
        homeAddressText.setText("");
        homeAddressText.setText("");
        emailText.setText("");
        nursingHouseText.setText("");
        Location.setText("");
        scaleText.setText("");
        phoneNumberText.setText("");
         dB4OUtil.storeSystem(system);
        }else{
            JOptionPane.showMessageDialog(null, "Sorry, you cannot register this nursing house name!");
        }
    }//GEN-LAST:event_registerBtnActionPerformed

