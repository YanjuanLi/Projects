    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         //TODO add your handling code here:
        int selectedRow = tblEnterprise.getSelectedRow();
        if (selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would you like to delete the enterprise?", "Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                NursingHouse nursingHouse = (NursingHouse)tblEnterprise.getValueAt(selectedRow,0);
                for(Network network : system.getNetworkList()){
                network.getNursingHouseDirectory().removeNursingHouse(nursingHouse);
                }
                populateEnterpriseTable();
            }
        }
            else {
                JOptionPane.showMessageDialog(null,"Please select a row from table first","Warning",JOptionPane.WARNING_MESSAGE);
            }
        populateComboBox();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

