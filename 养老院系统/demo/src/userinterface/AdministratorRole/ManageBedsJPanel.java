    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblBed.getSelectedRow();
        if (selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would you like to delete the account details?", "Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Bed bed = (Bed)tblBed.getValueAt(selectedRow,0);
                
                for(Network network:system.getNetworkList()){
                    for(NursingHouse nursingHouse:network.getNursingHouseDirectory().getNursingHouseList()){
                        if(nursingHouse.getName().equals(s)){
                            for(Elder elder:nursingHouse.getElderDirectory().getElderList()){
                                if(elder.getBedId()==bed.getBedId()){
                                    elder.setBedId(0);
                                }
                            }
                        }
                    }
                }
                Bed be = null;
                for(Network network: system.getNetworkList()){
                    for(NursingHouse nh:network.getNursingHouseDirectory().getNursingHouseList()){
                        if(nh.getName().equals(s)){
                        for(Bed b : nh.getBedList().getBedList()){
                            if(b==bed){
                                be=b;
                            }
                        }
                        nh.getBedList().removeBed(be);
                        }
                        
                    }
                }
                //system.getBedList().removeBed(bed);
                populateBedTable();
            }
            else {
                JOptionPane.showMessageDialog(null,"Please select a row from table first","Warning",JOptionPane.WARNING_MESSAGE);
            }
        }
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed


