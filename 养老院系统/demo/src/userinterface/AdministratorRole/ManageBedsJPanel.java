    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try{
            if(txtBedNo.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Please input bed number! ");
            }else{
        for(Network network: system.getNetworkList()){
            for(NursingHouse nursingHouse : network.getNursingHouseDirectory().getNursingHouseList()){
                if(nursingHouse.getName().equals(s)){ 
                    Bed bed = null;
                       for(Bed b : nursingHouse.getBedList().getBedList()){
                           if(Integer.parseInt(txtBedNo.getText()) == b.getBedId()){
                              bed=b;
                           } 
                }
                       if(nursingHouse.getBedList().getBedList().contains(bed)){
                           JOptionPane.showMessageDialog(null,"Bed has existed! ");
                       }else{
                              Bed d = nursingHouse.getBedList().addBed();
                              d.setElderName("Unknown");
                              d.setNursingWorkerName("Unkown");
                              d.setBedId(Integer.parseInt(txtBedNo.getText()));
                              JOptionPane.showMessageDialog(null,"Bed created successfully! ");
                       }
                    }
                }
            }}
        }
        
        catch(NumberFormatException nfe){
           JOptionPane.showMessageDialog(null,"BedId Only Numbers allowed");
       }
        txtBedNo.setText("");
        populateBedTable();
    }//GEN-LAST:event_btnAddActionPerformed

